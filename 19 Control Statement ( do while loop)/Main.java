/*====================do While loop======================*/
/*=========================================
3 parts of (for loop)
1/ initialization
2/ Condition
3/ Increment/ decrement part
===========================================*/
public class Main{
    public static void main(String[] args){
        int i = 1;
        do{
            System.out.println(i+" Bangladesh");
            i++;
        }while(i<=10);
    }
} 
