/*====================For loop======================*/
/*=========================================
3 parts of (for loop)
1/ initialization
2/ Condition
3/ Increment/ decrement part
===========================================*/
public class Main
{
	public static void main(String[] args) {
		for(int i = 1; i <= 10; i++){
		    System.out.println(i);
		}
		
		for(int j = 5; j<= 20; j=j+2){
		    System.out.println(j);
		}
	}
}

