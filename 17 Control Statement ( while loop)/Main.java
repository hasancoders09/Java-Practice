/*====================While loop======================*/
/*=========================================
3 parts of (for loop)
1/ initialization
2/ Condition
3/ Increment/ decrement part
===========================================*/
public class Main{
    public static void main(String[] args){
        int i = 1;
        while(i<=100){
            System.out.println(i);
            i = i+2;
        }
    }
}
